function merge(firstArr, secondArr) {
  let newArr = firstArr 
  for (let i = 0; i < secondArr.length; i++) {
    firstArr.push(secondArr[i]);
  }
  return newArr.sort();
}

console.log(merge([ 4, 5, 6 ], [ 1, 2, 3, 4 ]), "=?", [ 1, 2, 3, 4, 4, 5, 6 ]);
console.log(merge([ 4 ], [ 2, 5, 8 ]), "=?", [ 2, 4, 5, 8 ]);
console.log(merge([ 1, 2, 6 ], []), "=?", [ 1, 2, 6 ]);